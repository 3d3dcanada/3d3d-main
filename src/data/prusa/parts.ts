// ============ UTILITY FUNCTIONS ============
export function getPartsByCategory(category: string): PrusaPart[] {
  return PRUSA_PARTS.filter(p => p.category === category);
}

export function getPartsByPrinterCompatibility(printerId: string): PrusaPart[] {
  return PRUSA_PARTS.filter(p => p.compatiblePrinters.includes(printerId));
}

export function getPartById(id: string): PrusaPart | undefined {
  return PRUSA_PARTS.find(p => p.id === id);
}

export function getEssentialParts(): PrusaPart[] {
  return PRUSA_PARTS.filter(p => p.isEssential);
}

export function getUpgradeParts(): PrusaPart[] {
  return PRUSA_PARTS.filter(p => p.isUpgrade);
}

